# Створіть функцію, яка приймає список з елементів типу int, а повертає новий список з рядкових значень вихідного
# масиву. Додайте анотацію типів для вхідних і вислідних значень функції.

def func(lst: list[int]):
    lst2: list[str] = []
    for _ in lst:
        lst2.append(str(_))
    return lst2


print(func([10, 12, 20]))
