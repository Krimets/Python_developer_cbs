# введеного користувачем числа.

import math

print(f'Факторіал числа n =', math.factorial(int(input('Число: '))))

# АБО
f = 1
n = int(input('Число: '))
if n == 0:
    print(f'Факторіал числа n = {1}')
else:
    for i in range(1, n + 1):
        f *= i
    print(f'Факторіал числа n = {f}')
