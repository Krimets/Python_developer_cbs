# Напишіть рекурсивну функцію, яка обчислює суму натуральних чисел, які входять до заданого проміжку.

def summa(i, n):
    if n == i:
        return i
    else:
        n += summa(i, n - 1)
    return n


num = int(input('''Введіть початок
>>> '''))
num2 = int(input('''Введіть кінець
>>> '''))
if num < 1:
    num = 1
if num2 < 1:
    print("Натуральних чисел у проміжку не має")
else:
    print("Сума натуральних чисел проміжку:", summa(num, num2))
