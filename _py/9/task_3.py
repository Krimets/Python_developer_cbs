# Нехай на кожну сходинку можна стати з попередньої або переступивши через одну. Визначте, скількома способами
# можна піднятися на задану сходинку.


def fib(n):
    if n == 1 or n == 2:
        return n
    else:
        return fib(n - 1) + fib(n - 2)


print('На задану сходинку можна піднятися', fib(int(input('''Введіть номер сходинки:
>>> '''))), 'способами(-ом)')
